package kotlina.study.delegates

// 创建接口
interface Base {
	fun print()
}

// 实现此接口的被委托的类
class BaseImpl(val x: Int) : Base {
	override fun print() { print(x) }
}

// 通过关键字 by 建立委托类
class Derived(b: Base) : Base by b

fun main(args: Array<String>) {
	val b = BaseImpl(10)
	val derived = Derived(b)
	derived.print()
}